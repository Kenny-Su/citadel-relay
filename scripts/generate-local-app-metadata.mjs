import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { parseCitadelPackageMetadata } from './generate-bundled-apps.mjs';
import { readLocalExternalAppsConfig } from './local-external-apps.mjs';

const rootDir = join(dirname(fileURLToPath(import.meta.url)), '..');
const outputFileName = 'generatedMetadata.ts';
const header = '// Generated by scripts/generate-local-app-metadata.mjs from package.json#citadel.\n';

function usage() {
  return [
    'Usage: node scripts/generate-local-app-metadata.mjs [--check] [--package-dir <dir>]',
    '',
    'Generates app runtime metadata source from each local app package.json#citadel block.'
  ].join('\n');
}

function parseArgs(argv) {
  const packageDirs = [];
  let check = false;

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];

    if (token === '--check') {
      check = true;
      continue;
    }

    if (token === '--package-dir') {
      const packageDir = argv[index + 1];

      if (!packageDir) {
        throw new Error(`${usage()}\n\n--package-dir requires a value`);
      }

      packageDirs.push(resolve(process.cwd(), packageDir));
      index += 1;
      continue;
    }

    throw new Error(`${usage()}\n\nUnknown option: ${token}`);
  }

  return {
    check,
    packageDirs
  };
}

function literal(value) {
  return JSON.stringify(value);
}

export function generateLocalAppMetadataSource(packageDir) {
  const packageJsonPath = join(packageDir, 'package.json');
  const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf8'));
  const descriptor = parseCitadelPackageMetadata(packageJson.name, packageJson);

  return [
    header,
    "import type { AppManifest, AppPackageDescriptor } from '@citadel/platform/app';",
    '',
    'export const generatedManifest = {',
    `  appId: ${literal(descriptor.manifest.appId)},`,
    `  label: ${literal(descriptor.manifest.label)},`,
    `  defaultSpaceId: ${literal(descriptor.manifest.defaultSpaceId)},`,
    `  persistence: ${literal(descriptor.manifest.persistence)},`,
    `  version: ${literal(descriptor.manifest.version)}`,
    '} satisfies AppManifest;',
    '',
    'export const generatedAppPackage = {',
    '  appId: generatedManifest.appId,',
    '  manifest: generatedManifest,',
    `  packageName: ${literal(descriptor.packageName)},`,
    '  client: {',
    `    subpath: ${literal(descriptor.client.subpath)},`,
    `    registrationExport: ${literal(descriptor.client.registrationExport)}`,
    '  },',
    '  server: {',
    `    subpath: ${literal(descriptor.server.subpath)},`,
    `    registrationExport: ${literal(descriptor.server.registrationExport)}`,
    '  }',
    '} satisfies AppPackageDescriptor;',
    ''
  ].join('\n');
}

function localAppPackageDirs() {
  return readLocalExternalAppsConfig().packages.map((app) => resolve(rootDir, app.sourcePath));
}

export function generateLocalAppMetadata(options = {}) {
  const packageDirs = options.packageDirs?.length ? options.packageDirs : localAppPackageDirs();
  const staleFiles = [];

  for (const packageDir of packageDirs) {
    const outputPath = join(packageDir, 'src', outputFileName);
    const expectedSource = generateLocalAppMetadataSource(packageDir);
    const currentSource = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : undefined;

    if (options.check) {
      if (currentSource !== expectedSource) {
        staleFiles.push(outputPath);
      }

      continue;
    }

    writeFileSync(outputPath, expectedSource);
  }

  if (staleFiles.length > 0) {
    throw new Error([
      'Generated local app metadata is stale. Run npm run generate:local-app-metadata.',
      ...staleFiles.map((filePath) => `- ${filePath}`)
    ].join('\n'));
  }
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  try {
    generateLocalAppMetadata(parseArgs(process.argv.slice(2)));
  } catch (error) {
    console.error(error instanceof Error ? error.message : error);
    process.exitCode = 1;
  }
}
