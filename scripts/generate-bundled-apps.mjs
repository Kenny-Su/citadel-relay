import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join, relative } from 'node:path';

const rootDir = join(dirname(fileURLToPath(import.meta.url)), '..');
const configPath = join(rootDir, 'bundled-apps.json');
const outputPath = join(rootDir, 'src/bundledApps/generatedResolver.ts');
const checkOnly = process.argv.includes('--check');

function readConfig() {
  const config = JSON.parse(readFileSync(configPath, 'utf8'));

  if (!config || typeof config !== 'object' || !Array.isArray(config.packages)) {
    throw new Error('bundled-apps.json must contain a packages array');
  }

  if (!config.packages.every((packageName) => typeof packageName === 'string')) {
    throw new Error('bundled-apps.json packages must contain only strings');
  }

  return config;
}

function descriptorExportName(packageName) {
  const appName = packageName.split('/app-')[1];

  if (!appName) {
    throw new Error(`Cannot derive descriptor export name for bundled app package: ${packageName}`);
  }

  const identifier = appName
    .split('-')
    .map((part, index) => index === 0 ? part : `${part[0]?.toUpperCase() ?? ''}${part.slice(1)}`)
    .join('');

  return `${identifier}AppPackage`;
}

function generate(config) {
  const imports = config.packages.map((packageName, index) => (
    `import { ${descriptorExportName(packageName)} as bundledAppPackage${index} } from '${packageName}';`
  ));
  const entries = config.packages.map((packageName, index) => (
    `  '${packageName}': bundledAppPackage${index}`
  ));

  return [
    '// Generated by scripts/generate-bundled-apps.mjs. Do not edit by hand.',
    "import type { AppPackageDescriptor } from '@citadel/platform/app';",
    ...imports,
    '',
    'export const bundledAppDescriptorByPackageName: Record<string, AppPackageDescriptor> = {',
    entries.join(',\n'),
    '};',
    ''
  ].join('\n');
}

const nextSource = generate(readConfig());

if (checkOnly) {
  const currentSource = readFileSync(outputPath, 'utf8');

  if (currentSource !== nextSource) {
    throw new Error(`${relative(rootDir, outputPath)} is stale. Run npm run generate:bundled-apps.`);
  }
} else {
  writeFileSync(outputPath, nextSource);
}
